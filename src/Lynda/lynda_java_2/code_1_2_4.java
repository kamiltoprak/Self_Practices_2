package Lynda.lynda_java_2;

public class code_1_2_4 {
    public static void main(String[] args) {
        System.out.println(1 & 2 );
        // 001 & 010 => 000
        System.out.println(1 & 3 );
        // 001 & 011 => 001
        System.out.println(1 | 3 );
        // 001 | 011 => 011
        System.out.println(1 ^ 3 );
        // 001 ^ 011 => 010
    }
}
