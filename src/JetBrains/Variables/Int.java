package JetBrains.Variables;

public class Int {
    public static void main(String[] args) {
        int n=0;
        System.out.print(++n);
        System.out.print(n++);
        System.out.println(n++);
        char ch = '\u0031' + '\u0035';
        System.out.println(ch);
        char c = '1' + '3';
        System.out.println(c);
    }
}
