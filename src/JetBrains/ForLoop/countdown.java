package JetBrains.ForLoop;

public class countdown {
    public static void main(String[] args) {
        //Soru 01:
//1' den 37 e kadar olan sayıları birer boşluk ara ve aralarında küçüktür işareti ile konsolo yazdıran program;
        int i;
        for (i = 1;i <= 36;i++){
            System.out.print(i+ " < ");
        }
        System.out.println(37);
    }

}
